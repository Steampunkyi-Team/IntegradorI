    private void btnInicioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInicioActionPerformed
        datos(usuario,password);
        if(usuario.equals(txtUsuario.getText()) && password.equals(txtContra.getText())){
            Formulacion Ingreso=new Formulacion();
            Ingreso.setVisible(true);
            this.dispose();
        }else if(txtUsuario.getText().compareTo(usuario)!=0 && txtContra.getText().compareTo(password)!=0){
            JOptionPane.showMessageDialog(this,"Usuario y/o Contraseña no válidos\nIngrese nuevamente");
            txtUsuario.setFocusable(true);
        }else if(txtContra.getText().compareTo(password)!=0){
            JOptionPane.showMessageDialog(this, "Contraseña no válida");
            txtContra.setFocusable(true);
        }
    }//GEN-LAST:event_btnInicioActionPerformed

